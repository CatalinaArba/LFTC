public class PifPair {
    String token;
    Integer positionInST;

    public PifPair(String token, Integer positionInST) {
        this.token = token;
        this.positionInST = positionInST;
    }

    public String getToken() {
        return token;
    }

    public Integer getPositionInST() {
        return positionInST;
    }
}
